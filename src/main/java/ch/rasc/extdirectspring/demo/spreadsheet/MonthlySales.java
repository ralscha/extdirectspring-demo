package ch.rasc.extdirectspring.demo.spreadsheet;

public class MonthlySales {
	private int id;
	private int year;
	private int jan;
	private int feb;
	private int mar;
	private int apr;
	private int may;
	private int jun;
	private int jul;
	private int aug;
	private int sep;
	private int oct;
	private int nov;
	private int dec;

	public void setId(int id) {
		this.id = id;
	}

	public void setYear(int year) {
		this.year = year;
	}

	public int getId() {
		return this.id;
	}

	public int getYear() {
		return this.year;
	}

	public int getJan() {
		return this.jan;
	}

	public void setJan(int jan) {
		this.jan = jan;
	}

	public int getFeb() {
		return this.feb;
	}

	public void setFeb(int feb) {
		this.feb = feb;
	}

	public int getMar() {
		return this.mar;
	}

	public void setMar(int mar) {
		this.mar = mar;
	}

	public int getApr() {
		return this.apr;
	}

	public void setApr(int apr) {
		this.apr = apr;
	}

	public int getMay() {
		return this.may;
	}

	public void setMay(int may) {
		this.may = may;
	}

	public int getJun() {
		return this.jun;
	}

	public void setJun(int jun) {
		this.jun = jun;
	}

	public int getJul() {
		return this.jul;
	}

	public void setJul(int jul) {
		this.jul = jul;
	}

	public int getAug() {
		return this.aug;
	}

	public void setAug(int aug) {
		this.aug = aug;
	}

	public int getSep() {
		return this.sep;
	}

	public void setSep(int sep) {
		this.sep = sep;
	}

	public int getOct() {
		return this.oct;
	}

	public void setOct(int oct) {
		this.oct = oct;
	}

	public int getNov() {
		return this.nov;
	}

	public void setNov(int nov) {
		this.nov = nov;
	}

	public int getDec() {
		return this.dec;
	}

	public void setDec(int dec) {
		this.dec = dec;
	}

}
